/*
 * Aurora512Update()
 *
 * updating function for AURORA-384/512
 *
 * input  : msg (msg_bitlen [bits])
 *          msg_bitlen
 *          hctx
 * output : hctx (updated context)
 */
static void Aurora512Update(AURORA_CTX *hctx, const BitSequence *msg, DataLength msg_bitlen)
{
  int byte_cnt = hctx->cnt / 8;
  DataLength msg_bytelen = msg_bitlen >> 3;
  DataLength msg_blklen;
  int bit_ofs = hctx->cnt & 0x07;
  int msg_ofs = ((int) (msg_bitlen & 0x07U) + bit_ofs) & 0x07;
  BitSequence tmp;
  int i;

  if((hctx->cnt < 0) || (hctx->cnt >= 512)){
    return ;
  }

  if(bit_ofs){
    /* previous data is not byte aligned */
    tmp = hctx->buff[byte_cnt];
    hctx->cnt -= bit_ofs;
    while(msg_bytelen--){
      hctx->buff[byte_cnt++] = tmp | (*msg >> bit_ofs);
      tmp = *msg++ << (8 - bit_ofs);
      hctx->cnt += 8;
      if(hctx->cnt >= 64){
	hctx->cnt = 0;
	byte_cnt = 0;
	hctx->blk_num++;
	if(hctx->blk_idx >= u64(0000000008080808)){
	  /* mixing function */
	  Aurora512MF(hctx, hctx->blk_idx);
	  hctx->blk_idx = u64(0000000000000000);
	}
	Aurora512CF(hctx, hctx->buff, hctx->blk_idx);
	hctx->blk_idx += u64(0000000001010101);
      }
    }
    hctx->buff[byte_cnt] = tmp & (0xffU << (8 - msg_ofs));
    hctx->cnt += msg_ofs;
  }else{
    /* previous data is byte aligned */
    /* computation for extra block */
    if((byte_cnt) && (msg_bytelen >= (DataLength) (AURORA_BLK_SIZE - byte_cnt))){
      msg_bytelen -= (DataLength) (AURORA_BLK_SIZE - byte_cnt);
      for(i = byte_cnt; i < AURORA_BLK_SIZE; i++){
	hctx->buff[i] = *msg++;
      }
      hctx->cnt = 0;
      byte_cnt = 0;
      hctx->blk_num++;
      if(hctx->blk_idx >= u64(0000000008080808)){
	/* mixing function */
	Aurora512MF(hctx, hctx->blk_idx);
	hctx->blk_idx = u64(0000000000000000);
      }
      Aurora512CF(hctx, hctx->buff, hctx->blk_idx);
      hctx->blk_idx += u64(0000000001010101);
    }
    
    /* main computation */
    msg_blklen = msg_bytelen / (DataLength) AURORA_BLK_SIZE;
    hctx->blk_num += msg_blklen;
    while(msg_blklen--){
      if(hctx->blk_idx >= u64(0000000008080808)){
	/* mixing function */
	Aurora512MF(hctx, hctx->blk_idx);
	hctx->blk_idx = u64(0000000000000000);
      }
      Aurora512CF(hctx, msg, hctx->blk_idx);
      hctx->blk_idx += u64(0000000001010101);
      msg += AURORA_BLK_SIZE;
    }
    
    msg_bytelen %= (DataLength) AURORA_BLK_SIZE; /* remaining bytes */
    
    /* copy remaining data to local buffer */
    hctx->cnt += (int) (msg_bytelen * 8);
    while(msg_bytelen--){
      hctx->buff[byte_cnt++] = *msg++;
    }

    if(msg_bitlen & 0x07U){
      /* input data is not byte aligned */
      hctx->buff[byte_cnt] = (*msg & (0xffU << (8 - (int) (msg_bitlen & 0x07U))));
      hctx->cnt += (int) (msg_bitlen & 0x07U);
    }
  }
}

/*
 * Aurora512Final()
 *
 * finalization function for AURORA-512
 *
 * input  : hctx
 * output : digest
 */
static void Aurora512Final(BitSequence *digest, AURORA_CTX *hctx)
{
  int byte_cnt = hctx->cnt / 8;
  int i;

  if((hctx->cnt < 0) || (hctx->cnt >= 512)){
    return ;
  }

  if(0 != hctx->cnt){
    hctx->blk_num++;
  }

  if(hctx->cnt & 0x07){
    hctx->buff[byte_cnt++] |= 0x80U >> (hctx->cnt & 0x07);
  }else{
    hctx->buff[byte_cnt++] = 0x80U;
  }

  if(hctx->cnt > 447){
    /* computation for extra block */
    for(i = byte_cnt; i < AURORA_BLK_SIZE; i++){
      hctx->buff[i] = 0U;
    }
    if(hctx->blk_idx >= u64(0000000008080808)){
      /* mixing function */
      Aurora512MF(hctx, hctx->blk_idx);
      hctx->blk_idx = u64(0000000000000000);
    }
    Aurora512CF(hctx, hctx->buff, hctx->blk_idx);
    hctx->blk_idx += u64(0000000001010101);
    byte_cnt = 0;
  }

  /* computation for final block */
  for(i = byte_cnt; i < (AURORA_BLK_SIZE - 8); i++){
    hctx->buff[i] = 0U;
  }

  WRITE64BIT_OPT64(hctx->buff + AURORA_BLK_SIZE - 8, hctx->blk_num);

  /* final compression */
  if(hctx->blk_idx >= u64(0000000008080808)){
    /* mixing function */
    Aurora512MF(hctx, hctx->blk_idx);
    hctx->blk_idx = u64(0000000000000000);
  }
  Aurora512CF(hctx, hctx->buff, hctx->blk_idx);
  hctx->blk_idx += u64(0000000001010101);

  /* final mixing */
  Aurora512MF(hctx, u64(0000000009090909));

  WRITE64BIT_OPT64(digest + 0,  hctx->h[0]);
  WRITE64BIT_OPT64(digest + 8,  hctx->h[1]);
  WRITE64BIT_OPT64(digest + 16, hctx->h[2]);
  WRITE64BIT_OPT64(digest + 24, hctx->h[3]);

  WRITE64BIT_OPT64(digest + 32, hctx->h[4]);
  WRITE64BIT_OPT64(digest + 40, hctx->h[5]);
  WRITE64BIT_OPT64(digest + 48, hctx->h[6]);
  WRITE64BIT_OPT64(digest + 56, hctx->h[7]);
}

/*
 * Aurora384Final()
 *
 * finalization function for AURORA-384
 *
 * input  : hctx
 * output : digest
 */
static void Aurora384Final(BitSequence *digest, AURORA_CTX *hctx)
{
  BitSequence digest512[AURORA512_DIGEST_SIZE];
  int cnt = 0;
  int i, j;

  Aurora512Final(digest512, hctx);
  for(i = 0; i < 8; i++){
    for(j = 0; j < 6; j++){
      *digest++ = digest512[cnt++];
    }
    cnt += 2;
  }
}


