/*
 * AuroraInit0()
 *
 * initialization function for AURORA-256, AURORA-512
 * input and output: hctx
 */
static void AuroraInit0(AURORA_CTX *hctx)
{
  int i;
  
  for(i = 0; i < 8; i++){
    hctx->h[i] = u64(0000000000000000);
  }
  hctx->blk_num = u64(0000000000000000);
  hctx->cnt = 0;
  hctx->blk_idx = u64(0000000000000000);
}

/*
 * AuroraInit1()
 *
 * initialization function for AURORA-224, AURORA-384
 * input and output: hctx
 */
static void AuroraInit1(AURORA_CTX *hctx)
{
  int i;
  
  for(i = 0; i < 8; i++){
    hctx->h[i] = u64(ffffffffffffffff);
  }
  hctx->blk_num = u64(0000000000000000);
  hctx->cnt = 0;
  hctx->blk_idx = u64(0000000000000000);
}

/*
 * Aurora256Update()
 *
 * updating function for AURORA-224/256
 *
 * input  : msg (msg_bitlen [bits])
 *          msg_bitlen
 *          hctx
 * output : hctx (updated context)
 */
static void Aurora256Update(AURORA_CTX *hctx, const BitSequence *msg, DataLength msg_bitlen)
{
  int byte_cnt = hctx->cnt / 8;
  DataLength msg_bytelen = msg_bitlen >> 3;
  DataLength msg_blklen;
  int bit_ofs = hctx->cnt & 0x07;
  int msg_ofs = ((int) (msg_bitlen & 0x07U) + bit_ofs) & 0x07;
  BitSequence tmp;
  int i;

  if((hctx->cnt < 0) || (hctx->cnt >= 512)){
    return ;
  }

  if(bit_ofs){
    /* previous data is not byte aligned */
    tmp = hctx->buff[byte_cnt];
    hctx->cnt -= bit_ofs;
    while(msg_bytelen--){
      hctx->buff[byte_cnt++] = tmp | (*msg >> bit_ofs);
      tmp = *msg++ << (8 - bit_ofs);
      hctx->cnt += 8;
      if(hctx->cnt >= 64){
	hctx->cnt = 0;
	byte_cnt = 0;
	hctx->blk_num++;
	Aurora256CF(hctx, hctx->buff, u64(0000000000000000));
      }
    }
    hctx->buff[byte_cnt] = tmp & (0xffU << (8 - msg_ofs));
    hctx->cnt += msg_ofs;
  }else{
    /* previous data is byte aligned */
    /* computation for extra block */
    if((byte_cnt) && (msg_bytelen >= (DataLength) (AURORA_BLK_SIZE - byte_cnt))){
      msg_bytelen -= (DataLength) (AURORA_BLK_SIZE - byte_cnt);
      for(i = byte_cnt; i < AURORA_BLK_SIZE; i++){
	hctx->buff[i] = *msg++;
      }
      hctx->cnt = 0;
      byte_cnt = 0;
      hctx->blk_num++;
      Aurora256CF(hctx, hctx->buff, u64(0000000000000000));
    }
    
    /* main computation */
    msg_blklen = msg_bytelen / (DataLength) AURORA_BLK_SIZE;
    hctx->blk_num += msg_blklen;
    while(msg_blklen--){
      Aurora256CF(hctx, msg, u64(0000000000000000));
      msg += AURORA_BLK_SIZE;
    }
    
    msg_bytelen %= (DataLength) AURORA_BLK_SIZE; /* remaining bytes */
    
    /* copy remaining data to local buffer */
    hctx->cnt += (int) (msg_bytelen * 8);
    while(msg_bytelen--){
      hctx->buff[byte_cnt++] = *msg++;
    }

    if(msg_bitlen & 0x07U){
      /* input data is not byte aligned */
      hctx->buff[byte_cnt] = (*msg & (0xffU << (8 - (int) (msg_bitlen & 0x07U))));
      hctx->cnt += (int) (msg_bitlen & 0x07U);
    }
  }
}

/*
 * Aurora256Final()
 *
 * finalization function for AURORA-256
 *
 * input  : hctx
 * output : digest
 */
static void Aurora256Final(BitSequence *digest, AURORA_CTX *hctx)
{
  int byte_cnt = hctx->cnt / 8;
  int i;

  if((hctx->cnt < 0) || (hctx->cnt >= 512)){
    return ;
  }

  if(0 != hctx->cnt){
    hctx->blk_num++;
  }

  if(hctx->cnt & 0x07){
    hctx->buff[byte_cnt++] |= 0x80U >> (hctx->cnt & 0x07);
  }else{
    hctx->buff[byte_cnt++] = 0x80U;
  }

  if(hctx->cnt > 447){
    /* computation for extra block */
    for(i = byte_cnt; i < AURORA_BLK_SIZE; i++){
      hctx->buff[i] = 0U;
    }
    Aurora256CF(hctx, hctx->buff, u64(0000000000000000));
    byte_cnt = 0;
  }

  /* computation for final block */
  for(i = byte_cnt; i < (AURORA_BLK_SIZE - 8); i++){
    hctx->buff[i] = 0U;
  }

  WRITE64BIT_OPT64(hctx->buff + AURORA_BLK_SIZE - 8, hctx->blk_num);

  Aurora256CF(hctx, hctx->buff, u64(0000000001010101));

  WRITE64BIT_OPT64(digest + 0,  hctx->h[0]);
  WRITE64BIT_OPT64(digest + 8,  hctx->h[1]);
  WRITE64BIT_OPT64(digest + 16, hctx->h[2]);
  WRITE64BIT_OPT64(digest + 24, hctx->h[3]);
}

/*
 * Aurora224Final()
 *
 * finalization function for AURORA-224
 *
 * input  : hctx
 * output : digest
 */
static void Aurora224Final(BitSequence *digest, AURORA_CTX *hctx)
{
  BitSequence digest256[AURORA256_DIGEST_SIZE];
  int cnt = 0;
  int i, j;

  Aurora256Final(digest256, hctx);
  for(i = 0; i < 4; i++){
    for(j = 0; j < 7; j++){
      *digest++ = digest256[cnt++];
    }
    cnt++;
  }
}


